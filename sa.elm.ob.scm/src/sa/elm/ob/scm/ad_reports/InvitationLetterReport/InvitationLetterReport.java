package sa.elm.ob.scm.ad_reports.InvitationLetterReport;

public class InvitationLetterReport {

  private String suppliersId = "";
  private String bpName = "";
  private String bpAddress = "";

  public String getSuppliersId() {
    return suppliersId;
  }

  public void setSuppliersId(String id) {
    suppliersId = id;
  }

  public String getBpName() {
    return bpName;
  }

  public void setBpName(String name) {
    bpName = name;
  }

  public String getBpAddress() {
    return bpAddress;
  }

  public void setBpAddress(String addr) {
    bpAddress = addr;
  }

}
